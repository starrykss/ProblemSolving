sum = int(input())
diff = int(input())
print((sum + diff) // 2)
print((sum - diff) // 2)

# Klaudia : a, Natalia : b
# sum = a + b
# diff = a - b  
# 
#  a = sum - b
#  a = diff + b
# ---------------
# 2a = sum + diff  => a = (sum + diff) // 2 
#
#  b = sum - a
#  b = a - diff
# ----------------
# 2b = sum - diff  => b = (sum - diff) // 2

